const withoutPermissions = (
  permissions: readonly string[],
  removedPermissions: readonly string[],
): string[] => {
  const removedPermissionSet = new Set(removedPermissions);
  return permissions.filter((permission) => !removedPermissionSet.has(permission));
};

const uniquePermissions = (permissions: readonly string[]): string[] => {
  return Array.from(new Set(permissions)).sort((left, right) =>
    left.localeCompare(right),
  );
};

