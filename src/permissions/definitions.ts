export type PermissionDefinition = {
  description: string;
  key: string;
  label: string;
};

export type PermissionGroupDefinition = {
  key: string;
  label: string;
  permissions: readonly PermissionDefinition[];
};

const definePermissions = (
  groupKey: string,
  groupLabel: string,
  permissions: Array<[key: string, label: string, description: string]>,
): PermissionGroupDefinition => {
  return {
    key: groupKey,
    label: groupLabel,
    permissions: permissions.map(([key, label, description]) => ({
      description,
      key,
      label,
    })),
  };
};

